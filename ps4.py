def Max(a=input('Ввести строчку_1:'), b=input('Ввести строчку_1:')):
    """функція для виводу максимального числа"""
    if a > b:   #тут порівняння чисел або одне буде більше або інше
        print(a, 'Max')
    elif a == b:
        print(a, 'Midlle', b)
    else:
        print(b, 'Max')
Max()